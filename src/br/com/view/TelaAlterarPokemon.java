    public void PesquisarPokemon() {
        try {
            p.setId(Integer.valueOf(jTNDEX.getText()));
            dao.PesquisarRegistroPokemon(p);
            JOptionPane.showMessageDialog(null, p.getNome());
            jTgeracao.setText(String.valueOf(p.getGeracao()));
            jTnome.setText(p.getNome());
            jTNDEX.setEditable(false);

        } catch (Exception e) {
            System.out.println("Erro " + e.getMessage());
        }
    }

    public void AlterarPokemon() {
        try {

            p.setId(Integer.valueOf(jTNDEX.getText()));
            p.setNome(jTnome.getText());
            p.setGeracao(Integer.valueOf(jTgeracao.getText()));
            dao.EditarPokemon(p);

            jTnome.setText("");
            jTgeracao.setText("");
            jTNDEX.setText("");
            jTNDEX.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro de AlterarPokemon: " + e.getMessage());
        }
        VoltarMenuPrincipal();
    }
    public void VoltarMenuPrincipal(){
        TelaBoasVindas principal = new TelaBoasVindas();
        principal.setVisible(true);
        dispose();
    }

