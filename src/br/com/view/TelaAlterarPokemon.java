    public void restaurarDadosComboHabilidade() {
        try {
            ResultSet rs = ManterComboBox.comboBoxHabilidade();

            while (rs.next()) {
                id_habilidade.addElement(rs.getInt(1));
                jChabilidade.addItem(rs.getString(2));
            }

        } catch (Exception e) {
            System.out.println("Erro " + e.getMessage());
        }

    }

